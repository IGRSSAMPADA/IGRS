public class OTTDetailDTO {
	private String serialNo;
	private String ottNumber;
	private String registrationNo;
	private String generatedDate;
	private String expirationDate;
	private String status;

	/**
	 * @return the serialNo
	 */
	public String getSerialNo() {
		return serialNo;
	}

	/**
	 * @param serialNo
	 *            the serialNo to set
	 */
	public void setSerialNo(String serialNo) {
		this.serialNo = serialNo;
	}

	/**
	 * @return the ottNumber
	 */
	public String getOttNumber() {
		return ottNumber;
	}

	/**
	 * @param ottNumber
	 *            the ottNumber to set
	 */
	public void setOttNumber(String ottNumber) {
		this.ottNumber = ottNumber;
	}

	/**
	 * @return the generatedDate
	 */
	public String getGeneratedDate() {
		return generatedDate;
	}

	/**
	 * @param generatedDate
	 *            the generatedDate to set
	 */
	public void setGeneratedDate(String generatedDate) {
		this.generatedDate = generatedDate;
	}

	/**
	 * @return the expirationDate
	 */
	public String getExpirationDate() {
		return expirationDate;
	}

	/**
	 * @param expirationDate
	 *            the expirationDate to set
	 */
	public void setExpirationDate(String expirationDate) {
		this.expirationDate = expirationDate;
	}

	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}

	/**
	 * @param status
	 *            the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}

	/**
	 * @return the registrationNo
	 */
	public String getRegistrationNo() {
		return registrationNo;
	}

	/**
	 * @param registrationNo the registrationNo to set
	 */
	public void setRegistrationNo(String registrationNo) {
		this.registrationNo = registrationNo;
	}
}
