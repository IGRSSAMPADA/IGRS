package com.wipro.igrs.newreginit.dto;

public class UsesPojo {
	

	private String bio;
	private String pa;
	private String pfa;
	private String pi;
	private String pin;
	private String otp;
	

	public String getBio() {
		return bio;
	}
	public void setBio(String bio) {
		this.bio = bio;
	}
	public String getPa() {
		return pa;
	}
	public void setPa(String pa) {
		this.pa = pa;
	}
	public String getPfa() {
		return pfa;
	}
	public void setPfa(String pfa) {
		this.pfa = pfa;
	}
	public String getPi() {
		return pi;
	}
	public void setPi(String pi) {
		this.pi = pi;
	}
	public String getPin() {
		return pin;
	}
	public void setPin(String pin) {
		this.pin = pin;
	}
	public String getOtp() {
		return otp;
	}
	public void setOtp(String otp) {
		this.otp = otp;
	}


}
