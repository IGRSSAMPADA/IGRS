package com.wipro.igrs.Seals.action;

public class DataBean {
	public String lang;
	public String path;
	public String plotFinancialYear;
	public String plotDistrict;
	public String guideID;
	public String getLang() {
		return lang;
	}
	public void setLang(String lang) {
		this.lang = lang;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public String getPlotFinancialYear() {
		return plotFinancialYear;
	}
	public void setPlotFinancialYear(String plotFinancialYear) {
		this.plotFinancialYear = plotFinancialYear;
	}
	public String getPlotDistrict() {
		return plotDistrict;
	}
	public void setPlotDistrict(String plotDistrict) {
		this.plotDistrict = plotDistrict;
	}
	public String getGuideID() {
		return guideID;
	}
	public void setGuideID(String guideID) {
		this.guideID = guideID;
	}
	
	
}
